public class SwitchCase {
    public static void main(String[] args) {
        String name = "Chamudi";

        switch (name){
            case  "Chamudi":
                System.out.println("Hi.........");
                break;
            case "Nuwan":
                    System.out.println("Hello.......");
                    break;
            case "Saduni":
                System.out.println("Ayobowan....");
                break;
            default:
                System.out.println("No matching Item Found......");

        }
    }
}
